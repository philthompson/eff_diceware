words = sorted(words)

error_found = False
# remove non- 'a-z' characters... if any word does not end up empty, we have an error

valid_chars = set([
'a','b','c','d','e','f','g','h','i','j','k','l','m',
'n','o','p','q','r','s','t','u','v','w','x','y','z'])
for x in range(0, len(words)):
	for char in words[x]:
		if not char in valid_chars:
			error_found = True
			print("%s contains one or more invalid characters" % words[x])
			continue

if error_found:
	print("one or more words contain invalid characters")
	sys.exit(1)

# no word can start with any other word
for x in range(0, len(words)):
	for y in range(0, len(words)):
		if x == y:
			continue
		if words[y].startswith(words[x]):
			error_found = True
			remainder = words[y][len(words[x]):]
			print("%s starts with %s, leaving %s" % (words[y], words[x], remainder))
			for z in range(0, len(words)):
				if words[z] == remainder:
					print("(and %s is itself a word)" % remainder)
				elif words[z].startswith(remainder):
					remainder2 = words[z][len(remainder):]
					if remainder2 in words:
						print("(and %s ends with %s which itself is a word)" % (words[z], remainder2))

if error_found:
	print("one or more words are a duplicate or prefix of another word")
	sys.exit(1)

